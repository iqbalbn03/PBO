package Praktikum.pert3;

public class Relasional {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Operator relasional
        System.out.println("\033[H\033[2J");
        System.out.println("a == b: " + (a == b));   // Sama dengan
        System.out.println("a != b: " + (a != b));   // Tidak sama dengan
        System.out.println("a > b: " + (a > b));     // Lebih besar dari
        System.out.println("a < b: " + (a < b));     // Lebih kecil dari
        System.out.println("a >= b: " + (a >= b));   // Lebih besar atau sama dengan
        System.out.println("a <= b: " + (a <= b));   // Lebih kecil atau sama dengan
    }
}
